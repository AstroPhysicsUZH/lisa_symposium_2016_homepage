 
<p>
  Please be aware, that Switzerland has it's own currency, the Swiss Franc (CHF), and not adapted the Euro.
  There are some places where you can pay with Euros, and get the change back in Swiss Francs, but at a very unfavourable rate.
  We adivse to get money from ATM's or change in a bank. ATMs are available everywhere, there is one on the campus.
</p>

<p class="warning">
  In the university (cafeteria) you can only pay with Swiss Francs in <b>cash</b>.
  No credit / debit cards can be accepted.
</p>
