
<h2 id="confdin">Conference Dinner</h2>

<p>
    The conference dinner will take place on Wednesday evening, starting at 19:00 on the top of the Uetliberg.
</p>

<h3>How to get there by public transport</h3>
<p>
    Go to Zurich, main station (<span class="zvvstation">Bahnhofplatz | HB</span>) and take the SZU train <span class='zvvlinie sb'>S10</span> leaving from Track 22.
    (The conference ticket is valid, but please note that accompanying person need an additional ticket for 1-2 extra zone besides city zone.)
    The train takes about 20min to the final station, <span class="zvvstation">Uetliberg</span> (1h from the conference).
    <hr>
    from 17:38, <span class="zvvstation">Milchbuck</span> Tram <span class='zvvlinie l14'>14</span><br>
    to 17:50 <span class="zvvstation">Bahnhofplatz | HB</span>
    <hr>
    from 18:05, <span class="zvvstation">Zurich HB</span> Train <span class='zvvlinie sb'>S10</span><br>
    to 17:50 <span class="zvvstation">Uetliberg</span> (final station)

</p>
<h3>How to get there by hikingt</h3>
<p>
    The way up the hill is also a nice, but a little steep hike through the forest.
    Hiking shoes are not required.
    <br>
    Take the tram <span class='zvvlinie l14'>14</span>, bus <span class='zvvlinie l33'>33</span> or train <span class='zvvlinie sb'>S10</span> to station <span class="zvvstation">Triemli</span> and follow the signs upwards.
    The hike takes about 1 - 1.5 hours.
    <hr>
    Meeting point 1:
    <br>
    17:00 in front of the lecture hall
    <hr>
    Meeting point 2:
    <br>
    17:45 at end station of tram <span class='zvvlinie l14'>14</span> <span class="zvvstation">Triemli</span>

</p>

