
<p>
  From the tams stations <span class="zvvstation">Milchbuck</span> or <span class="zvvstation">Universität Irchel</span>, just follow the signs through the park to the Irchel Campus. (It's a 10min walk, there are a few stairs)
</p>
<p>
  Alternativly, for the mobility impaired, please note that usually at least every second tram is wheelchair friendly accessible.
  Exit at the station <span class="zvvstation">Universität Irchel</span> and follow the blue line on the map below.
  Follow the sidewalk on the street level into the parking house / gymn halls (ASVZ).
  Under the bridge, at the runabout turn right.
  Stay on the right side of the underground parking field and enter the main building, taking the elevator to floor G.
</p>

