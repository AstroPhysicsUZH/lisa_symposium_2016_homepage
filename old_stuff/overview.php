   <h3>Welcome to the website of the eLISA Consortium Meeting / Testing Alternative Theories of Gravity with LPF Workshop in Zurich</h3>

Dear eLISA Consortium members,<br><br>

the year 2013 has brought a big step forward with the choice of 
&laquo;The Gravitational Universe&raquo;
as an approved Science Theme with a firm launch slot as L3 mission in the ESA
program. 

The next step after the successful LISA Pathfinder flight in 2015 will be the
selection
of the specific mission concept to be flown. To make rapid progress in this direction it has been decided by the Consortium Board to 
held on 12 and 13 March a full eLISA Consortium Meeting 
which will be held in Zurich.

On 10 March we will also have a workshop
on &laquo;Testing Alternative Theories of Gravity with LPF&raquo;.

   
   

   
