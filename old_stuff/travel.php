<h2>Travelling in Zurich</h2>

With a single ticket / day pass / multi day pass for zone 110 you can travel freely with trams and buses within Zurich.
If you are not sure whether you are leaving the zone boundaries, consult <a target="_blank" href="http://www.zvv.ch/opencms/export/sites/default/common-images/content-image-gallery/linien-zonen-pdfs/Tarifzonen_2013.pdf">this</a>
document. You can get a zone 110 day pass for <b>CHF 8.40</b>. Unfortunately, there are only reduced multi day passes for six days: if you stay for a shorter period,
please get a new single ticket (valid for 1h) or day pass every morning (two single tickets cost already the same as a day pass).

