<h2>How to get to the Irchel campus</h2>

<h3>From Zurich Airport <img src="10.jpg" height="20"></h3>

From the airport take <b>tram number 10</b>, direction: <b>Z&uuml;rich, Bahnhofplatz/HB</b>. Make sure you are not travelling
to the other end, 'Z&uuml;rich Flughafen, Fracht'.
You can get out both at the stops <b>Milchbuck</b> or <b>Universit&auml;t Irchel</b>. Travel time is approximately 25 minutes.
Walk to the campus through the park
and then follow the signs. Do not forget to get a ticket for <b>'Stadt Z&uuml;rich'</b> (city ticket) at a vending machine for <b>CHF 6.60</b>
(corresponding to zones 110, 121). 
When getting back to the airport, make sure that the tram direction reads 'Z&uuml;rich Flughafen, Fracht',
as a few trams turn around already in Oerlikon. Get out at the <b>'Z&uuml;rich Flughafen'</b> station. From the Milchbuck or Universit&auml;t Irchel station you can use
the shortcut <b>'Zurich Airport'</b> or <b>'Z&uuml;rich Flughafen'</b> to get a ticket back to the airport.
<br><br>


<h3>From Zurich Mainstation <img src="10.jpg" height="20"> <img src="7.jpg" height="20"> <img src="14.jpg" height="20"></h3>

Take either <b>tram number 7</b> (direction 'Stettbach, Bahnhof') or <b>14</b> (direction 'Z&uuml;rich, Seebach') and exit at <b>Milchbuck</b>.
Travel time is approximately 15 minutes.
Walk to the campus through the park and then follow the signs. Also tram number 10 stops at <b>Milchbuck</b> or <b>Universit&auml;t Irchel</b>
but takes slightly longer (direction 'Z&uuml;rich Flughafen, Fracht').
Do not forget to get a ticket for 'Zone 110' (sometimes abbreviated as 'Zone 10') at a vending machine for <b>CHF 4.20</b>.

